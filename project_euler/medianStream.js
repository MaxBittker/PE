var MedianFinder = function() {
    this.less = new heap((a, b) => a < b)
    this.more = new heap((a, b) => a > b)

};

/**
 * @param {integer} word
 * @return {void}
 * Adds a num into the data structure.
 */
MedianFinder.prototype.addNum = function(num) {
    if (num >= this.findMedian()) {
        this.more.add(num)
    }

    if (num < this.findMedian()) {
        this.less.add(num)
    }
    this.balance()
    return (num)
};
MedianFinder.prototype.balance = function() {
        if (Math.abs(this.less.array.length - this.more.array.length) < 2)
            return
        if (this.less.array.length > this.more.array.length)
            this.more.add(this.less.popRoot())
        else
            this.less.add(this.more.popRoot())
        this.balance()
    }
    /**
     * @return {double}
     * Return median of current data stream
     */
MedianFinder.prototype.findMedian = function() {
    var lslen = this.less.array.length - 1
    var mrlen = this.more.array.length - 1

    if (mrlen === 0 && lslen === 0)
        return Infinity
    if (mrlen === lslen)
        return ((this.less.peek() + this.more.peek()) / 2)
    if (mrlen > lslen)
        return this.more.peek()

    return this.less.peek()
};

